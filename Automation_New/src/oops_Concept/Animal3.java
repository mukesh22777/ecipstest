
// Example 
// add values in object memory  using reference variable 
package oops_Concept;

public class Animal3 {
	String color;
	int Age;
	
	public static void main(String[] args) {
		Animal3 buzo = new Animal3();
		buzo.color = "Black";
		buzo.Age = 10;
		System.out.println(buzo.color + buzo.Age);
		
	}
}


